#include "ft_printf.h"

int		set_flag(t_box *box, char c)
{
	char *all;

	if (box->zero == 0)
		all = "-0";
	else
		all = "-";
	while (*all)
	{
		if (c == *all)
			return (1);
		all++;
	}
	return (0);
}
